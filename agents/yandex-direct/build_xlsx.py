SECTION_FONT = Font(bold=True, color="FFFFFF", size=12)
SUB_FILL = PatternFill("solid", fgColor="EEEEEE")
SUB_FONT = Font(bold=True, size=11)
WRAP = Alignment(wrap_text=True, vertical="top")

def style_header_row(ws, row_idx, n_cols):
    for c in range(1, n_cols + 1):
        cell = ws.cell(row=row_idx, column=c)
        cell.fill = HEAD_FILL
        cell.font = HEAD_FONT
        cell.alignment = WRAP
        cell.border = BORDER

def section(ws, row_idx, title, span=8):
    ws.cell(row=row_idx, column=1, value=title)
    ws.merge_cells(start_row=row_idx, start_column=1, end_row=row_idx, end_column=span)
    cell = ws.cell(row=row_idx, column=1)
    cell.fill = SECTION_FILL
    cell.font = SECTION_FONT
    cell.alignment = Alignment(vertical="center", horizontal="left")
    ws.row_dimensions[row_idx].height = 22

def subhead(ws, row_idx, title, span=8):
    ws.cell(row=row_idx, column=1, value=title)
    ws.merge_cells(start_row=row_idx, start_column=1, end_row=row_idx, end_column=span)
    cell = ws.cell(row=row_idx, column=1)
    cell.fill = SUB_FILL
    cell.font = SUB_FONT
    cell.alignment = Alignment(vertical="center", horizontal="left")

def autosize(ws, widths):
    for i, w in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w


