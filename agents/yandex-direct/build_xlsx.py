def write_group_sheet(wb, sheet_title, meta, keys, minus_words, ads, creatives_briefs, custom_extensions=None):
    ws = wb.create_sheet(title=sheet_title)
    autosize(ws, [22, 22, 22, 22, 22, 14, 14, 14])
    r = 1

    # Метаданные
    section(ws, r, f"📋 {meta['title']}")
    r += 1
    for k, v in meta["fields"].items():
        ws.cell(row=r, column=1, value=k).font = Font(bold=True)
        ws.cell(row=r, column=2, value=v).alignment = WRAP
        ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8)
        ws.cell(row=r, column=1).border = BORDER
        ws.cell(row=r, column=2).border = BORDER
        r += 1
    r += 1

    # Ключи
    section(ws, r, "🔑 Ключевые слова и приоритеты")
    r += 1
    headers_k = ["Ключ", "Частотность", "Приоритет"]
    for c, h in enumerate(headers_k, start=1):
        ws.cell(row=r, column=c, value=h)
    style_header_row(ws, r, len(headers_k))
    r += 1
    for key, freq, prio in keys:
        ws.cell(row=r, column=1, value=key).border = BORDER
        ws.cell(row=r, column=2, value=freq).border = BORDER
        ws.cell(row=r, column=3, value=prio).border = BORDER
        ws.cell(row=r, column=1).alignment = WRAP
        if prio == "П1":
            ws.cell(row=r, column=3).fill = PatternFill("solid", fgColor="FFD6D6")
        elif prio == "П2":
            ws.cell(row=r, column=3).fill = PatternFill("solid", fgColor="FFF3CC")
        elif prio.startswith("П3"):
            ws.cell(row=r, column=3).fill = PatternFill("solid", fgColor="E0E0E0")
        r += 1
    r += 1

    # Минус-слова
    section(ws, r, "🚫 Минус-слова")
    r += 1
    ws.cell(row=r, column=1, value=minus_words).alignment = WRAP
    ws.merge_cells(start_row=r, start_column=1, end_row=r, end_column=8)
    ws.row_dimensions[r].height = 40
    r += 2

    # Объявления
    if ads:
        section(ws, r, "📝 Объявления")
        r += 1
        for ad_block in ads:
            subhead(ws, r, f"{ad_block['type']} · {ad_block['name']}")
            r += 1
            for field, val in ad_block["fields"].items():
                ws.cell(row=r, column=1, value=field).font = Font(bold=True)
                ws.cell(row=r, column=2, value=val).alignment = WRAP
                ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8)
                ws.cell(row=r, column=1).border = BORDER
                ws.cell(row=r, column=2).border = BORDER
                r += 1
            r += 1

    # Расширения
    section(ws, r, "🔗 Расширения")
    r += 1
    if custom_extensions:
        for ext_block in custom_extensions:
            subhead(ws, r, ext_block["name"])
            r += 1
            for item in ext_block["items"]:
                ws.cell(row=r, column=1, value=item).alignment = WRAP
                ws.merge_cells(start_row=r, start_column=1, end_row=r, end_column=8)
                ws.cell(row=r, column=1).border = BORDER
                r += 1
            r += 1
    else:
        ws.cell(row=r, column=1, value="См. лист «Расширения · общие» — универсальный набор БС с описаниями + уточнения.").alignment = WRAP
        ws.merge_cells(start_row=r, start_column=1, end_row=r, end_column=8)
        ws.cell(row=r, column=1).font = Font(italic=True)
        r += 2

    # ТЗ креативов
    if creatives_briefs:
        section(ws, r, "🖼 ТЗ креативов (1080×1080)")
        r += 1
        for brief in creatives_briefs:
            subhead(ws, r, brief["name"])
            r += 1
            for field, val in brief["fields"].items():
                ws.cell(row=r, column=1, value=field).font = Font(bold=True)
                ws.cell(row=r, column=2, value=val).alignment = WRAP
                ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=8)
                ws.cell(row=r, column=1).border = BORDER
                ws.cell(row=r, column=2).border = BORDER
                r += 1
            r += 1

    return ws


